DROP TABLE IF EXISTS orgs;
CREATE TABLE orgs (
  id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  org_name varchar NOT NULL,
  admin int null REFERENCES users(id) ON DELETE CASCADE
);
ALTER TABLE users
  ADD COLUMN 
    org int REFERENCES orgs(id) ON DELETE SET NULL;
  DROP TABLE IF EXISTS teams;
  CREATE TABLE teams(
    id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    team_name varchar NOT null,
    admin INT REFERENCES users(id) ON DELETE SET NULL,
    member_of INT REFERENCES orgs(id) ON DELETE SET NULL
  );
  ALTER Table users
  Add COLUMN
    team INT REFERENCES teams(id) ON DELETE SET NULL;